def find_perimeter():
    l1 = float(input("enter the length of first side of triangle:"))
    l2 = float(input("enter the length of second side of triangle:"))
    l3 = float(input("enter the length of third side of triangle:"))
    perimeter = l1+l2+l3
    print(f"the perimeter of triangle is {perimeter}")
find_perimeter()